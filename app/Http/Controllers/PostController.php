    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $post = Post::find($id);
        if ($post->user->id == Auth::user()->id) $post->delete();
        else return redirect(route('home'))->with('error', 'Fail delete post ' . $post->title . 'this is not your post');

        return redirect(route('home'))->with('success', 'Success delete post ' . $post->title);
    }
